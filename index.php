	$(function () { // wait for document ready
		// init
		var controller = new ScrollMagic.Controller();

		// define movement of panels
		var wipeAnimation = new TimelineMax()
			// animate to second panel
			.to("#slideContainer", 0.5, {z: -2000})		// move back in 3D space
			.to("#slideContainer", 1,   {x: "-20%"})	// move in to first panel
			.to("#slideContainer", 0.5, {z: 0})				// move back to origin in 3D space
		    // animate to third panel
			.to("#slideContainer", 0.5, {z: -2000, delay: 1})
			.to("#slideContainer", 1,   {x: "-40%"})
			.to("#slideContainer", 0.5, {z: 0})
			// animate to forth panel
			.to("#slideContainer", 0.5, {z: -2000, delay: 1})
			.to("#slideContainer", 1,   {x: "-60%"})
			.to("#slideContainer", 0.5, {z: 0})
             // animate to fifth panel
            .to("#slideContainer", 0.5, {z: -2000, delay:1})
            .to("#slideContainer", 1,   {x:"-80%"})
            .to("#slideContainer", 0.5, {z:0});            
		// create scene to pin and link animation
		new ScrollMagic.Scene({
				triggerElement: "#pinContainer",
				triggerHook: "onLeave",
				duration: "500%"
			})
			.setPin("#pinContainer")
			.setTween(wipeAnimation)
			.addTo(controller);
	});
